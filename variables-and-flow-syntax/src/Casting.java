
public class Casting {
	public static void main(String[] args) {
		double salary = 1270.5;
		float floatingPoint = (float) 3.1; // or float floatingPoint = 3.1f;
		int value = (int) salary;
		
		System.out.println(value);
		
		long longNumber = 2912912121212L;
		short shortNumber = 2131;
		byte byteNumber = 124;
		
		double value1 = 0.2;
		double value2 = 0.1;
		double sum = value1 + value2;
		
		System.out.println(sum);
	}
}
