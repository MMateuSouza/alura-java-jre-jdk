public class Variables {
	public static void main(String[] args) {
		System.out.println("Teste de Variáveis");
		
		// Estaticamente e Fortemente Tipada
		int age;
		age = 24;
		
		System.out.println(age);
		
		age = 24 + 1;
		
		System.out.println(age);
		
		age = 7 * 5 + 2;
		
		System.out.println(age);
		
		System.out.println("A idade é " + age + ", parabéns!");
	}
}
