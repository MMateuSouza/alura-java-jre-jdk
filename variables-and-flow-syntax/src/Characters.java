
public class Characters {
	public static void main(String[] args) {
		// Requer aspas simples
		char letter = 'A';
		
		System.out.println(letter);
		
		char value = 66;
		System.out.println(value);
		
		value = (char) (value + 1);
		System.out.println(value);
		
		// Requer aspas duplas
		String name = "Mateus";
		System.out.println(name);
		
		name = name + 13;
		System.out.println(name);
	}
}
